#pragma once

#include <string_view>

namespace mizcore {

enum class ERROR_TYPE
{
    SUCCESS = 0,
    UNKNOWN = 999,

    // Tokenizer and block parser errors
    BEGIN_KEYWORD_IN_STATEMENT,
    BLOCK_NOT_CLOSED_IN_ARTICLE,
    BLOCK_NOT_IN_SECTION,
    BLOCK_START_KEYWORD_IN_STATEMENT,
    CASE_BLOCK_PREV_SIBLING_NOT_CASE_BLOCK,
    END_KEYWORD_IN_STATEMENT,
    END_KEYWORD_WITH_NO_PAIR_BLOCK,
    END_KEYWORD_WITHOUT_SEMICOLON,
    ENVIRON_KEYWORD_AFTER_BEGIN_KEYWORD,
    ENVIRON_KEYWORD_IN_STATEMENT,
    NOT_AFTER_BEGIN_KEYWORD,
    NOT_JUST_BELOW_ROOT_BLOCK,
    NOT_UNDER_PROOF_BLOCK,
    NOW_BLOCK_STARTS_WITH_IRREGULAR_TOKENS,
    PER_CASES_STATEMENT_MISSING,
    PROOF_START_WITHOUT_PROPOSITION,
    ROOT_BLOCK_UNDER_BLOCK,
    SCHEME_KEYWORD_IN_STATEMENT,
    STATEMENT_IS_EMPTY,
    STATEMENT_NOT_CLOSED_IN_ARTICLE,
    SUPPOSE_BLOCK_PREV_SIBLING_NOT_SUPPOSE_BLOCK,
    TOKEN_IS_UNKNOWN,
};

enum class ERROR_LEVEL
{
    ERROR,
    WARNING,
    INFO,
};

std::string_view
GetErrorMessage(ERROR_TYPE error);

int
GetErrorCode(ERROR_TYPE error);

ERROR_LEVEL
GetErrorLevel(ERROR_TYPE error);

std::string_view
GetErrorLevelText(ERROR_LEVEL level);

} // namespace mizcore
